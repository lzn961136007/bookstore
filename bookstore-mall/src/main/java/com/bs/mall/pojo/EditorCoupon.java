package com.bs.mall.pojo;

public class EditorCoupon {
	private Integer couponId;
	private Integer userId;
	private String couponUseTime;


	
	public Integer getCouponId() {
		return couponId;
	}
	public void setCouponId(Integer couponId) {
		this.couponId = couponId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getCouponUseTime() {
		return couponUseTime;
	}
	public void setCouponUseTime(String couponUseTime) {
		this.couponUseTime = couponUseTime;
	}
	public EditorCoupon(Integer couponId, Integer userId, String couponUseTime) {
		super();
		this.couponId = couponId;
		this.userId = userId;
		this.couponUseTime = couponUseTime;
	}
	public EditorCoupon() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "EditorCoupon [couponId=" + couponId + ", userId=" + userId + ", couponUseTime=" + couponUseTime + "]";
	}
	
}
