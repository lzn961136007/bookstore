package com.bs.mall.pojo;

public class Express {
	private Integer expressId;
	private Integer expressNumbers;
	private String expressName;
	private double expressCost;
	private String expressStatus;
	public Integer getExpressId() {
		return expressId;
	}
	public void setExpressId(Integer expressId) {
		this.expressId = expressId;
	}
	public Integer getExpressNumbers() {
		return expressNumbers;
	}
	public void setExpressNumbers(Integer expressNumbers) {
		this.expressNumbers = expressNumbers;
	}
	public String getExpressName() {
		return expressName;
	}
	public void setExpressName(String expressName) {
		this.expressName = expressName;
	}
	public double getExpressCost() {
		return expressCost;
	}
	public void setExpressCost(double expressCost) {
		this.expressCost = expressCost;
	}
	public String getExpressStatus() {
		return expressStatus;
	}
	public void setExpressStatus(String expressStatus) {
		this.expressStatus = expressStatus;
	}
	public Express(Integer expressId, Integer expressNumbers, String expressName, double expressCost,
			String expressStatus) {
		super();
		this.expressId = expressId;
		this.expressNumbers = expressNumbers;
		this.expressName = expressName;
		this.expressCost = expressCost;
		this.expressStatus = expressStatus;
	}
	public Express() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "Express [expressId=" + expressId + ", expressNumbers=" + expressNumbers + ", expressName=" + expressName
				+ ", expressCost=" + expressCost + ", expressStatus=" + expressStatus + "]";
	}
	
	

}
