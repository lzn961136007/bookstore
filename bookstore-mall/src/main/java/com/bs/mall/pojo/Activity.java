package com.bs.mall.pojo;

public class Activity {
	private Integer activityId;
	private String activityName;
	private Integer activityDiscount;
	private String activityStart;
	private String activityEnd;
	private Integer bookId;
	private Double discountPrice;
	private Double bookSalesPrice;
	public Integer getActivityId() {
		return activityId;
	}
	public void setActivityId(Integer activityId) {
		this.activityId = activityId;
	}
	public String getActivityName() {
		return activityName;
	}
	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
	public Integer getActivityDiscount() {
		return activityDiscount;
	}
	public void setActivityDiscount(Integer activityDiscount) {
		this.activityDiscount = activityDiscount;
	}
	public String getActivityStart() {
		return activityStart;
	}
	public void setActivityStart(String activityStart) {
		this.activityStart = activityStart;
	}
	public String getActivityEnd() {
		return activityEnd;
	}
	public void setActivityEnd(String activityEnd) {
		this.activityEnd = activityEnd;
	}
	public Integer getBookId() {
		return bookId;
	}
	public void setBookId(Integer bookId) {
		this.bookId = bookId;
	}
	public Double getDiscountPrice() {
		return discountPrice;
	}
	public void setDiscountPrice(Double discountPrice) {
		this.discountPrice = discountPrice;
	}
	public Double getBookSalesPrice() {
		return bookSalesPrice;
	}
	public void setBookSalesPrice(Double bookSalesPrice) {
		this.bookSalesPrice = bookSalesPrice;
	}
	@Override
	public String toString() {
		return "Activity [activityId=" + activityId + ", activityName=" + activityName + ", activityDiscount="
				+ activityDiscount + ", activityStart=" + activityStart + ", activityEnd=" + activityEnd + ", bookId="
				+ bookId + ", discountPrice=" + discountPrice + ", bookSalesPrice=" + bookSalesPrice + "]";
	}
	public Activity(Integer activityId, String activityName, Integer activityDiscount, String activityStart,
			String activityEnd, Integer bookId, Double discountPrice, Double bookSalesPrice) {
		super();
		this.activityId = activityId;
		this.activityName = activityName;
		this.activityDiscount = activityDiscount;
		this.activityStart = activityStart;
		this.activityEnd = activityEnd;
		this.bookId = bookId;
		this.discountPrice = discountPrice;
		this.bookSalesPrice = bookSalesPrice;
	}
	public Activity() {
		super();
		// TODO Auto-generated constructor stub
	}
	
}
