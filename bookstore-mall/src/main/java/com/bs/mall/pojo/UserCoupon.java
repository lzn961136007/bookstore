package com.bs.mall.pojo;

import java.util.Date;

public class UserCoupon {
	
	private Integer UserId;
	private Integer couponId;
	private String couponName;
	private Integer couponMoney;
	private Integer couponNum;
	private Date couponStart;
	private Date couponEnd;
	private boolean couponUseStatus;
	private Date couponGetTime;
	private Date couponUseTime;
	
	public Integer getUserId() {
		return UserId;
	}
	public void setUserId(Integer userId) {
		UserId = userId;
	}
	public Integer getCouponId() {
		return couponId;
	}
	public void setCouponId(Integer couponId) {
		this.couponId = couponId;
	}
	public String getCouponName() {
		return couponName;
	}
	public void setCouponName(String couponName) {
		this.couponName = couponName;
	}
	public Integer getCouponMoney() {
		return couponMoney;
	}
	public void setCouponMoney(Integer couponMoney) {
		this.couponMoney = couponMoney;
	}
	public Integer getCouponNum() {
		return couponNum;
	}
	public void setCouponNum(Integer couponNum) {
		this.couponNum = couponNum;
	}
	public Date getCouponStart() {
		return couponStart;
	}
	public void setCouponStart(Date couponStart) {
		this.couponStart = couponStart;
	}
	public Date getCouponEnd() {
		return couponEnd;
	}
	public void setCouponEnd(Date couponEnd) {
		this.couponEnd = couponEnd;
	}
	public boolean isCouponUseStatus() {
		return couponUseStatus;
	}
	public void setCouponUseStatus(boolean couponUseStatus) {
		this.couponUseStatus = couponUseStatus;
	}
	public Date getCouponGetTime() {
		return couponGetTime;
	}
	public void setCouponGetTime(Date couponGetTime) {
		this.couponGetTime = couponGetTime;
	}
	public Date getCouponUseTime() {
		return couponUseTime;
	}
	public void setCouponUseTime(Date couponUseTime) {
		this.couponUseTime = couponUseTime;
	}
	public UserCoupon(Integer userId, Integer couponId, String couponName, Integer couponMoney, Integer couponNum,
			Date couponStart, Date couponEnd, boolean couponUseStatus, Date couponGetTime, Date couponUseTime) {
		super();
		UserId = userId;
		this.couponId = couponId;
		this.couponName = couponName;
		this.couponMoney = couponMoney;
		this.couponNum = couponNum;
		this.couponStart = couponStart;
		this.couponEnd = couponEnd;
		this.couponUseStatus = couponUseStatus;
		this.couponGetTime = couponGetTime;
		this.couponUseTime = couponUseTime;
	}
	public UserCoupon() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "UserCoupon [UserId=" + UserId + ", couponId=" + couponId + ", couponName=" + couponName
				+ ", couponMoney=" + couponMoney + ", couponNum=" + couponNum + ", couponStart=" + couponStart
				+ ", couponEnd=" + couponEnd + ", couponUseStatus=" + couponUseStatus + ", couponGetTime="
				+ couponGetTime + ", couponUseTime=" + couponUseTime + "]";
	}
	
	
}
