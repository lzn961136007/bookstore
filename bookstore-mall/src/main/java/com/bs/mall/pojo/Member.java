package com.bs.mall.pojo;

public class Member {
	private Integer MemberPrimaryId;
	private Integer memberId;
	private String memberName;
	private Double memberMoney;
	private Integer memberBook;
	public Integer getMemberPrimaryId() {
		return MemberPrimaryId;
	}
	public void setMemberPrimaryId(Integer memberPrimaryId) {
		MemberPrimaryId = memberPrimaryId;
	}
	public Integer getMemberId() {
		return memberId;
	}
	public void setMemberId(Integer memberId) {
		this.memberId = memberId;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public Double getMemberMoney() {
		return memberMoney;
	}
	public void setMemberMoney(Double memberMoney) {
		this.memberMoney = memberMoney;
	}
	public Integer getMemberBook() {
		return memberBook;
	}
	public void setMemberBook(Integer memberBook) {
		this.memberBook = memberBook;
	}
}
