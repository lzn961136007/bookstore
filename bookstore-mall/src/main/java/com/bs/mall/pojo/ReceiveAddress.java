package com.bs.mall.pojo;

public class ReceiveAddress {
	private Integer addressId;
	private Integer userId;
	private String addressMobile;
	private String addressConsignee;
	private String addressProvince;
	private String addressCity;
	private String addressCounty;
	private String addressDetail;
	private boolean addressStatus;

	public boolean isAddressStatus() {
		return addressStatus;
	}

	public void setAddressStatus(boolean addressStatus) {
		this.addressStatus = addressStatus;
	}

	public Integer getAddressId() {
		return addressId;
	}

	public void setAddressId(Integer addressId) {
		this.addressId = addressId;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getAddressMobile() {
		return addressMobile;
	}

	public void setAddressMobile(String addressMobile) {
		this.addressMobile = addressMobile;
	}

	public String getAddressConsignee() {
		return addressConsignee;
	}

	public void setAddressConsignee(String addressConsignee) {
		this.addressConsignee = addressConsignee;
	}

	public String getAddressProvince() {
		return addressProvince;
	}

	public void setAddressProvince(String addressProvince) {
		this.addressProvince = addressProvince;
	}

	public String getAddressCity() {
		return addressCity;
	}

	public void setAddressCity(String addressCity) {
		this.addressCity = addressCity;
	}

	public String getAddressCounty() {
		return addressCounty;
	}

	public void setAddressCounty(String addressCounty) {
		this.addressCounty = addressCounty;
	}

	public String getAddressDetail() {
		return addressDetail;
	}

	public void setAddressDetail(String addressDetail) {
		this.addressDetail = addressDetail;
	}

	public ReceiveAddress(Integer addressId, Integer userId, String addressMobile, String addressConsignee,
			String addressProvince, String addressCity, String addressCounty, String addressDetail,boolean addressStatus) {
		super();
		this.addressId = addressId;
		this.userId = userId;
		this.addressMobile = addressMobile;
		this.addressConsignee = addressConsignee;
		this.addressProvince = addressProvince;
		this.addressCity = addressCity;
		this.addressCounty = addressCounty;
		this.addressDetail = addressDetail;
		this.addressStatus=addressStatus;
	}

	public ReceiveAddress() {
		super();
	}

	@Override
	public String toString() {
		return "ReceiveAddress [addressId=" + addressId + ", userId=" + userId + ", addressMobile=" + addressMobile
				+ ", addressConsignee=" + addressConsignee + ", addressProvince=" + addressProvince + ", addressCity="
				+ addressCity + ", addressCounty=" + addressCounty + ", addressDetail=" + addressDetail +",addressStatus="+addressStatus+ "]";
	}

}
