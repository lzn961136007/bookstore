package com.bs.mall.util;

public class JsonData {
	private String key;
	private Object value;
	private String msg;
	private Boolean status;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Boolean getStatus() {
		return status;
	}
	public void setStatus(Boolean status) {
		this.status = status;
	}
	@Override
	public String toString() {
		return "JsonData [key=" + key + ", value=" + value + ", msg=" + msg + ", status=" + status + "]";
	}
	public JsonData() {
	}
	public JsonData(String key, Object value, String msg, Boolean status) {
		this.key = key;
		this.value = value;
		this.msg = msg;
		this.status = status;
	}
}
